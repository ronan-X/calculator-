function abriraba() {
  var abriraba = document.querySelector('.pesquisar-aba-tudo')

  var fecharaba = document.querySelector('#fechar-aba')
  fecharaba.style.display = 'block'
  abriraba.style.display = 'block'
}

function fecharaba() {
  var abriraba = document.querySelector('.pesquisar-aba-tudo')

  abriraba.style.display = 'none'
}

